	public bool IsActive
	{
		get
		{
			return _isActive;
		}
		set
		{
			if (value == _isActive)
			{
				return;
			}
			List<Team> list = Mission.Current.Teams.Where((Team t) => IsUsableBy(t.Side)).ToList();
			_isActive = value;
			foreach (Team item in list)
			{
				if (item.TeamAI != null)
				{
					if (_isActive)
					{
						item.TeamAI.AddStrategicArea(this);
					}
					else
					{
						item.TeamAI.RemoveStrategicArea(this);
					}
				}
			}
		}
	}

