		num *= MissionGameModels.Current.AgentStatCalculateModel.GetDetachmentCostMultiplierOfAgent(candidate, this);
		List<float> list = oldValue ?? new List<float>(_capacity);
		list.Clear();
		for (int i = 0; i < _capacity; i++)
