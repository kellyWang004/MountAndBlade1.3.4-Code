	private int CalculateCapacity()
	{
		return TaleWorlds.Library.MathF.Max(1, TaleWorlds.Library.MathF.Ceiling(TaleWorlds.Library.MathF.Max(1f, _width) * TaleWorlds.Library.MathF.Max(1f, _depth)));
	}

