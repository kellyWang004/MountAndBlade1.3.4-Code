	public bool IsEquipmentEqualTo(Equipment other)
	{
		if (other == null)
		{
			return false;
		}
		for (int i = 0; i < 12; i++)
		{
			if (!this[i].IsEqualTo(other[i]))
			{
				return false;
			}
		}
		if (other.IsStealth == IsStealth && other.IsCivilian == IsCivilian)
		{
			return other.IsBattle == IsBattle;
		}
		return false;
	}

	public static Equipment GetRandomEquipmentElements(BasicCharacterObject character, bool randomEquipmentModifier, EquipmentType equipmentType, int seed = -1)
	{
		Equipment equipment = new Equipment(equipmentType);
		List<Equipment> list = new List<Equipment>();
		switch (equipmentType)
		{
		case EquipmentType.Battle:
			list = character.BattleEquipments.ToList();
			break;
		case EquipmentType.Civilian:
			list = character.CivilianEquipments.ToList();
			break;
		}
		if (list.IsEmpty())
		{
			return equipment;
		}
		int count = list.Count;
		Random random = new Random(seed);
		int weaponSetNo = MBRandom.RandomInt(count);
		int weaponSetNo2 = MBRandom.RandomInt(count);
		int weaponSetNo3 = MBRandom.RandomInt(count);
		for (int i = 0; i < 12; i++)
		{
			if (seed != -1)
			{
				weaponSetNo = random.Next() % count;
				weaponSetNo2 = random.Next() % count;
				weaponSetNo3 = random.Next() % count;
			}
			switch (i)
			{
			case 0:
			case 1:
				equipment[i] = GetRandomizedEquipment(list, (EquipmentIndex)i, weaponSetNo, randomEquipmentModifier);
				break;
			case 2:
			case 3:
				equipment[i] = GetRandomizedEquipment(list, (EquipmentIndex)i, weaponSetNo2, randomEquipmentModifier);
				break;
			default:
				equipment[i] = GetRandomizedEquipment(list, (EquipmentIndex)i, weaponSetNo3, randomEquipmentModifier);
				break;
			}
		}
		return equipment;
	}

