using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;

namespace TaleWorlds.CampaignSystem.Naval;

public class Figurehead : PropertyObject
{
	public EffectIncrementType EffectIncrementType { get; private set; }

	public float EffectAmount { get; private set; }

	public CultureObject Culture { get; private set; }

	internal static void AutoGeneratedStaticCollectObjectsFigurehead(object o, List<object> collectedObjects)
	{
		((Figurehead)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	public Figurehead(string stringId)
		: base(stringId)
	{
	}

	public void Initialize(TextObject name, TextObject description, float amount, CultureObject culture, EffectIncrementType effectIncrementType)
	{
		Initialize(name, description);
		EffectAmount = amount;
		Culture = culture;
		EffectIncrementType = effectIncrementType;
		AfterInitialized();
	}
}
