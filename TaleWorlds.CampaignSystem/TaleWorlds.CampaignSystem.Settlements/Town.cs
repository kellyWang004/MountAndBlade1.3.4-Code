	public IEnumerable<PartyBase> GetDefenderParties(MapEvent.BattleTypes battleType)
	{
		yield return base.Settlement.Party;
		foreach (MobileParty party in base.Settlement.Parties)
		{
			if (party.MapFaction.IsAtWarWith(base.Settlement.SiegeEvent.BesiegerCamp.MapFaction) && party.IsActive && !party.IsVillager && !party.IsCaravan && (!party.IsMilitia || (!InRebelliousState && battleType != MapEvent.BattleTypes.SallyOut)))
			{
				yield return party.Party;
			}
		}
	}

	public PartyBase GetNextDefenderParty(ref int partyIndex, MapEvent.BattleTypes battleType)
	{
		partyIndex++;
		if (partyIndex == 0)
		{
			return base.Settlement.Party;
		}
		for (int i = partyIndex - 1; i < base.Settlement.Parties.Count; i++)
		{
			MobileParty mobileParty = base.Settlement.Parties[i];
			if (mobileParty.MapFaction.IsAtWarWith(base.Settlement.SiegeEvent.BesiegerCamp.MapFaction) && mobileParty.IsActive && !mobileParty.IsVillager && !mobileParty.IsCaravan && (!mobileParty.IsMilitia || (!InRebelliousState && battleType != MapEvent.BattleTypes.SallyOut)))
			{
				partyIndex = i + 1;
				return mobileParty.Party;
			}
		}
		return null;
	}

