	public override void Deserialize(MBObjectManager objectManager, XmlNode node)
	{
		base.Deserialize(objectManager, node);
		_isCastle = node.Attributes["is_castle"] != null && bool.Parse(node.Attributes["is_castle"].Value);
		base.BackgroundCropPosition = float.Parse(node.Attributes["background_crop_position"].Value);
		base.BackgroundMeshName = node.Attributes["background_mesh"].Value;
		base.WaitMeshName = node.Attributes["wait_mesh"].Value;
		if (Campaign.Current.CampaignGameLoadingType != Campaign.GameLoadingType.SavedCampaign)
		{
			_prosperity = float.Parse(node.Attributes["prosperity"].Value);
		}
		BesiegerCampPositions1 = new MatrixFrame[1] { MatrixFrame.Identity };
		BesiegerCampPositions2 = new MatrixFrame[1] { MatrixFrame.Identity };
		if (Campaign.Current.CampaignGameLoadingType == Campaign.GameLoadingType.SavedCampaign)
		{
			return;
		}
		Buildings.Clear();
		foreach (XmlNode childNode in node.ChildNodes)
		{
			if (!childNode.Name.Equals("Buildings"))
			{
				continue;
			}
			foreach (XmlNode childNode2 in childNode.ChildNodes)
			{
				if (childNode2.Name.Equals("Building"))
				{
					string value = childNode2.Attributes["id"].Value;
					XmlAttribute xmlAttribute = childNode2.Attributes["level"];
					BuildingType buildingType = MBObjectManager.Instance.GetObject<BuildingType>(value);
					int currentLevel = buildingType.StartLevel;
					if (xmlAttribute != null)
					{
						currentLevel = int.Parse(xmlAttribute.Value);
					}
					Buildings.Add(new Building(buildingType, this, 0f, currentLevel));
				}
			}
		}
	}

