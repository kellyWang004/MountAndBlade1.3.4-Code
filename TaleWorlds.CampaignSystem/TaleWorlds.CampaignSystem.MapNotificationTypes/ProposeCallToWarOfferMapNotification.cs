using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes;

public class ProposeCallToWarOfferMapNotification : InformationData
{
	[SaveableProperty(1)]
	public Kingdom OfferedKingdom { get; private set; }

	[SaveableProperty(2)]
	public Kingdom KingdomToCallToWarAgainst { get; private set; }

	[SaveableProperty(3)]
	public CampaignTime TriggerTime { get; private set; }

	public override TextObject TitleText => new TextObject("{=bzV3Evgk}Propose Call to War Offer");

	public override string SoundEventPath => "event:/ui/notification/peace_offer";

	internal static void AutoGeneratedStaticCollectObjectsProposeCallToWarOfferMapNotification(object o, List<object> collectedObjects)
	{
		((ProposeCallToWarOfferMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(OfferedKingdom);
		collectedObjects.Add(KingdomToCallToWarAgainst);
		CampaignTime.AutoGeneratedStaticCollectObjectsCampaignTime(TriggerTime, collectedObjects);
	}

	internal static object AutoGeneratedGetMemberValueOfferedKingdom(object o)
	{
		return ((ProposeCallToWarOfferMapNotification)o).OfferedKingdom;
	}

	internal static object AutoGeneratedGetMemberValueKingdomToCallToWarAgainst(object o)
	{
		return ((ProposeCallToWarOfferMapNotification)o).KingdomToCallToWarAgainst;
	}

	internal static object AutoGeneratedGetMemberValueTriggerTime(object o)
	{
		return ((ProposeCallToWarOfferMapNotification)o).TriggerTime;
	}

	public ProposeCallToWarOfferMapNotification(Kingdom offeredKingdom, Kingdom kingdomToCallToWarAgainst, TextObject descriptionText)
		: base(descriptionText)
	{
		OfferedKingdom = offeredKingdom;
		KingdomToCallToWarAgainst = kingdomToCallToWarAgainst;
		TriggerTime = CampaignTime.Now + Campaign.Current.Models.AllianceModel.DurationForOffers;
	}

	public ProposeCallToWarOfferMapNotification(TextObject description)
		: base(description)
	{
	}

	public override bool IsValid()
	{
		return !TriggerTime.IsPast;
	}
}
