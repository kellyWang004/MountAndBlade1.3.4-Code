using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes;

public class AcceptCallToWarOfferMapNotification : InformationData
{
	[SaveableProperty(1)]
	public Kingdom OfferingKingdom { get; private set; }

	[SaveableProperty(2)]
	public Kingdom KingdomToCallToWarAgainst { get; private set; }

	[SaveableProperty(3)]
	public CampaignTime TriggerTime { get; private set; }

	public override TextObject TitleText => new TextObject("{=ywL1bGlZ}Accept Call To War Offer");

	public override string SoundEventPath => "event:/ui/notification/peace_offer";

	internal static void AutoGeneratedStaticCollectObjectsAcceptCallToWarOfferMapNotification(object o, List<object> collectedObjects)
	{
		((AcceptCallToWarOfferMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(OfferingKingdom);
		collectedObjects.Add(KingdomToCallToWarAgainst);
		CampaignTime.AutoGeneratedStaticCollectObjectsCampaignTime(TriggerTime, collectedObjects);
	}

	internal static object AutoGeneratedGetMemberValueOfferingKingdom(object o)
	{
		return ((AcceptCallToWarOfferMapNotification)o).OfferingKingdom;
	}

	internal static object AutoGeneratedGetMemberValueKingdomToCallToWarAgainst(object o)
	{
		return ((AcceptCallToWarOfferMapNotification)o).KingdomToCallToWarAgainst;
	}

	internal static object AutoGeneratedGetMemberValueTriggerTime(object o)
	{
		return ((AcceptCallToWarOfferMapNotification)o).TriggerTime;
	}

	public AcceptCallToWarOfferMapNotification(Kingdom offeringKingdom, Kingdom kingdomToCallToWarAgainst, TextObject descriptionText)
		: base(descriptionText)
	{
		OfferingKingdom = offeringKingdom;
		KingdomToCallToWarAgainst = kingdomToCallToWarAgainst;
		TriggerTime = CampaignTime.Now + Campaign.Current.Models.AllianceModel.DurationForOffers;
	}

	public AcceptCallToWarOfferMapNotification(TextObject description)
		: base(description)
	{
	}

	public override bool IsValid()
	{
		return !TriggerTime.IsPast;
	}
}
