using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes;

public class TributeFinishedMapNotification : InformationData
{
	[SaveableField(10)]
	private readonly IFaction _relatedFaction;

	public override TextObject TitleText => new TextObject("{=aItf9n8B}Tribute Payment Finished");

	public override string SoundEventPath => "event:/ui/notification/kingdom_decision";

	public IFaction RelatedFaction => _relatedFaction;

	internal static void AutoGeneratedStaticCollectObjectsTributeFinishedMapNotification(object o, List<object> collectedObjects)
	{
		((TributeFinishedMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(_relatedFaction);
	}

	internal static object AutoGeneratedGetMemberValue_relatedFaction(object o)
	{
		return ((TributeFinishedMapNotification)o)._relatedFaction;
	}

	public TributeFinishedMapNotification(TextObject description, IFaction relatedFaction)
		: base(description)
	{
		_relatedFaction = relatedFaction;
	}
}
