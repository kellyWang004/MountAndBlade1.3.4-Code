using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes;

public class AllianceOfferMapNotification : InformationData
{
	[SaveableProperty(1)]
	public Kingdom OfferingKingdom { get; private set; }

	[SaveableProperty(2)]
	public CampaignTime TriggerTime { get; private set; }

	public override TextObject TitleText => new TextObject("{=kuweQPQz}Alliance Offer");

	public override string SoundEventPath => "event:/ui/notification/peace_offer";

	internal static void AutoGeneratedStaticCollectObjectsAllianceOfferMapNotification(object o, List<object> collectedObjects)
	{
		((AllianceOfferMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(OfferingKingdom);
		CampaignTime.AutoGeneratedStaticCollectObjectsCampaignTime(TriggerTime, collectedObjects);
	}

	internal static object AutoGeneratedGetMemberValueOfferingKingdom(object o)
	{
		return ((AllianceOfferMapNotification)o).OfferingKingdom;
	}

	internal static object AutoGeneratedGetMemberValueTriggerTime(object o)
	{
		return ((AllianceOfferMapNotification)o).TriggerTime;
	}

	public AllianceOfferMapNotification(Kingdom offeringKingdom, TextObject descriptionText)
		: base(descriptionText)
	{
		OfferingKingdom = offeringKingdom;
		TriggerTime = CampaignTime.Now + Campaign.Current.Models.AllianceModel.DurationForOffers;
	}

	public AllianceOfferMapNotification(TextObject description)
		: base(description)
	{
	}

	public override bool IsValid()
	{
		return !TriggerTime.IsPast;
	}
}
