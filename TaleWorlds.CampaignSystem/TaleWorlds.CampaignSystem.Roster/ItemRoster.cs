				}
				else if (item.HorseComponent.IsMount)
				{
					NumberOfMounts += amount;
				}
				else if (item.HorseComponent.IsLiveStock)
				{
					NumberOfLivestockAnimals += amount;
					TotalFood += amount * item.HorseComponent.MeatCount;
				}
			}
			else if (item.IsFood)
			{
				TotalFood += amount;
				FoodVariety++;
