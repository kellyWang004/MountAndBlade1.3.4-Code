		}
		else if (troopIndex != targetIndex)
		{
			int num = ((troopIndex < targetIndex) ? 1 : (-1));
			TroopRosterElement troopRosterElement = data[troopIndex];
			for (int i = troopIndex; i != targetIndex; i += num)
