			PreviousDamagedTargetType = SiegeBombardTargets.None;
			PreviousTargetIndex = -1;
			AlreadyFired = false;
			LastBombardTime = CampaignTime.Never;
			CalculateNextFireTime(siegeSide.SiegeEvent, siegeSide.BattleSide);
