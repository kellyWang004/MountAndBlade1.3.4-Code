	public void CollectBannerPiece()
	{
		//IL_0055: Unknown result type (might be due to invalid IL or missing references)
		//IL_005f: Expected O, but got Unknown
		//IL_007f: Unknown result type (might be due to invalid IL or missing references)
		//IL_0089: Expected O, but got Unknown
		//IL_009a: Unknown result type (might be due to invalid IL or missing references)
		CollectedBannerPieceCount++;
		ItemObject val = null;
		if (CollectedBannerPieceCount == 1)
		{
			val = ((GameType)Campaign.Current).ObjectManager.GetObject<ItemObject>("dragon_banner_center");
		}
		else if (CollectedBannerPieceCount == 2)
		{
			val = ((GameType)Campaign.Current).ObjectManager.GetObject<ItemObject>("dragon_banner_dragonhead");
			MBInformationManager.ShowSceneNotification((SceneNotificationData)new FindingSecondBannerPieceSceneNotificationItem(Hero.MainHero));
		}
		else if (CollectedBannerPieceCount == 3)
		{
			val = ((GameType)Campaign.Current).ObjectManager.GetObject<ItemObject>("dragon_banner_handle");
			MBInformationManager.ShowSceneNotification((SceneNotificationData)new FindingThirdBannerPieceSceneNotificationItem());
		}
		if (val != null)
		{
			MobileParty.MainParty.ItemRoster.AddToCounts(new EquipmentElement(val, (ItemModifier)null, (ItemObject)null, true), 1);
		}
		StoryModeEvents.Instance.OnBannerPieceCollected();
	}

